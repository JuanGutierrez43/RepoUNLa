# Variables usadas en el ejemplo de sentecia if
a = 4
b = 7

# Comparaciones básica
if a < b:
    print("a es menor que b")
if a > b:
    print("a es mayor que b")

# Comparaciones <= y >=
if a <= b:
    print ("a es menor o igual que b")
if a >= b:
    print ("a es mayor o igual que b")

# Comparación si dos objetos son iguales o no
if a == b:
    print("a es igual que b")
if a != b:
    print("a es distinto que b")

# Indentación
if a == 1:
    print("Si a es uno, esto se imprimirá.")
    print("Lo mismo que esto.")
    print("Y que esto.")

print("Esto siempre se imprimirá ya que no está indentado.")
